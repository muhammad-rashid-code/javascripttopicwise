// // <p id="myParagraph">This is some text.</p>
// // <button onclick="readText()">Read Text</button>
// // <button onclick="setText()">Set Text</button>

// // <script>
// //   function readText() {
// //     var paragraph = document.getElementById("myParagraph");
// //     var text = paragraph.textContent;
// //     alert(text);
// //   }

// //   function setText() {
// //     var paragraph = document.getElementById("myParagraph");
// //     paragraph.textContent = "This is new text!";
// //   }
// // </script>
// // In this example, we have a paragraph element with an ID of "myParagraph". We also have two buttons, one to read the text and one to set the text.

// The readText() function gets the paragraph element by its ID, and then gets its text content using the textContent property. It then displays the text using an alert box.

// The setText() function also gets the paragraph element by its ID, but then sets its text content to "This is new text!" using the textContent property.

// By using the textContent property, we can read and set the text of any element that contains text, not just paragraph elements.