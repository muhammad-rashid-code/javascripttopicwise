//In JavaScript, you can test sets of conditions using logical operators such as && (and), || (or), and ! (not).
// //The && operator tests if all conditions are true. For example:
// let x=65;
// let y=10;
// if (x>0&&y>0) {console.log("both x and y are positive")
    // }
// //In this example, the && operator tests if both x > 0 and y > 0 are true. Since both conditions are true, the code inside the if block is executed, and the output will be "Both x and y are positive."

// //The || operator tests if at least one condition is true. For example:
// let x =-5
// let y=-10
// if (x>0||y>0) {console.log("Either x or y is positive.")
// }
// //In this example, the || operator tests if either x > 0 or y > 0 is true. Since x > 0 is true, the code inside the if block is executed, and the output will be "Either x or y is positive."

// //The ! operator tests if a condition is not true. For example:
// let x=9;
// if (!(x>10)) {console.log("x is not greater than 10.");
// }
// //In this example, the ! operator tests if x > 10 is not true. Since x > 10 is false, the code inside the if block is executed, and the output will be "x is not greater than 10."

//By using logical operators, you can test sets of conditions and create complex decision-making structures in your JavaScript code.