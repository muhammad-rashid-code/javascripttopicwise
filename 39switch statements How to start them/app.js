//In JavaScript, a switch statement is a control flow statement that allows us to test a variable against multiple cases and execute different code depending on the matched case. The basic syntax of a switch statement is as follows:

//To start a switch statement, we first use the switch keyword followed by an opening parenthesis ( and the expression we want to test against. The expression can be a variable, a value, or an expression that evaluates to a value. Then, we use the case keyword followed by a value that we want to test the expression against. We can have multiple case statements with different values. Finally, we use the default keyword followed by code to be executed if none of the case statements match.

